int		ft_is_prime(int nb);

int		ft_is_prime(int nb)
{
	int a;

	if (nb < 2)
		return (0);
	a = 2;
	while (a < nb)
	{
		if (nb % a == 0)
			return (0);
		a++;
	}
	return (1);
}
